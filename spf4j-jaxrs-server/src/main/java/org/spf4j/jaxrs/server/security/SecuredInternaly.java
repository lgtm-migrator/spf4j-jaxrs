package org.spf4j.jaxrs.server.security;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation that indicates that a resource is secured internally.
 * This is done usually by injecting the SecurityContext.
 * This annotation lets the AnnotationAuthorizationFilter know about this behavior to pass the req through.
 * @author Zoltan Farkas
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD, ElementType.TYPE})
public @interface SecuredInternaly {
}
